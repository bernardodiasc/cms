  <td width="505" valign="top" background="arquivos/imagens/bgnews.jpg"><p align="justify">Toco guitarra há pelo menos 18 anos e, obviamente,                 no decorrer desse tempo acumulei bastante equipamento.</p>
    <p align="justify"> Alguns eu uso sempre, outros quase nunca. Não acho que                 para se tirar um bom som de guitarra você precise de equipamentos                 caros ou de uma grande quantidade deles. Acredito sim, que você                 precise de um bom ouvido e de boas mãos.</p>
    <p align="justify"> Mas ter uma variedade de instrumentos e equipamentos me ajuda                 na hora de produzir uma gravação e até mesmo                 na hora de compor. Cada instrumento soa de uma forma diferente e                 me abre um mundo de sons e texturas. Cada um me inspira de uma forma                 diferente. Não toco do mesmo jeito em uma strato e em uma                 Les Paul. Tampouco com um som saturado e com um som limpo. O som                 muda e com ele o clima, a concepção e o fraseado.                 Por isso gosto de ter instrumentos diferentes e equipamentos diversos.</p>
    <p align="justify"> Nessa sessão eu resolvi falar um pouquinho de cada um,                 assim como disponibilizar um sound clip, para vocês poderem                 saber um pouco mais desses brinquedos. Espero que assim, possa ajudá-los                 passando um pouquinho da minha experiência pra vocês.</p>
